package com.dnd.app.domain.enums;

/**
 * Перечисление HomebrewStatus описывает перечисление домена, которое фиксирует допустимые значения игровой бизнес-логики.
 * Используется для сохранения явной роли элемента в бизнес-потоке приложения.
 */
public enum HomebrewStatus {
    DRAFT,
    PUBLISHED,
    ARCHIVED,
    /** На ревью (пре-модерация; задел под платный контент P3). */
    UNDER_REVIEW,
    /** Отклонён модератором — скрыт с витрины, недоступен для install/attach (пост-модерация P2-6). */
    REJECTED
}
