package com.dnd.app.domain.enums;

/**
 * Тип принудительного перемещения комбатанта (фаза 2.12): PUSH (оттолкнуть от источника),
 * PULL (притянуть к источнику), SLIDE (сдвинуть в произвольную сторону). Все три не тратят
 * перемещение цели и не провоцируют атак; телепорт обрабатывается отдельным сценарием.
 */
public enum ForcedMoveType {
    PUSH,
    PULL,
    SLIDE
}
